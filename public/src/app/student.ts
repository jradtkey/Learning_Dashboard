export class Student {

  first_name: string;
  last_name: string;
  birthdate: string;
  user_id: string;

  constructor (first_name, last_name, birthdate, user_id){
    this.first_name = first_name;
    this.last_name = last_name;
    this.birthdate = birthdate;
    this.user_id = user_id;
  }


}
